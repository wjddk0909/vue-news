<template>
  <div>
    <div v-for="job in jobs" :key="job.id">{{ job.title }}</div>
  </div>
</template>

<script>
import { fetchJobsList } from '@/api/index'

export default {
  data() {
    return {
      jobs: []
    }
  },
  created() {
    fetchJobsList()
    .then(response => {
      console.log(response.data);
      this.jobs = response.data;
    })
    .catch(error => {
      console.log(error)
    })
  }
};
</script>

